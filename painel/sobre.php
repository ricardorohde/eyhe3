             jQuery(document).ready(function(){
                 jQuery('#form-contato').submit(function(){
                     jQuery.ajax({
                         type: "POST",
                         url: "painel/engine/recebe_form_contato.php",
                         data: new FormData(this),
                         processData: false,
                         contentType: false,
                         beforeSend: function(){
                           swal({
                                  title: 'Aguarde..!',
                                  text: 'Aguarde enquanto trabalhamos..',
                                  imageUrl: 'painel/images/avatar.jpg'
                                });
                         },
                         success: function(data)
                         {
                           if(data == 'enviado'){
                             swal('Recebemos seu contato com sucesso!', 'Responderemos o mais breve possível :)', 'success');
                             $("#form-contato").trigger("reset");
                           }else{
                             swal('Ops..', data, 'error');
                           }

                         }
                     });

                     return false;
                 });
             });
      </script>

